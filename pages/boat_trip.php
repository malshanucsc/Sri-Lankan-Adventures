			<p><h3>BOAT TRIPS</h3> Boat riding is a very enjoyable activity to do when you are in Sri Lanka. It gives you an insight to the life of the people living along the river bank and also a chance to see the unique growth of plantation and the bio diversity integrated around the river region. You might be lucky enough to see Birds living around the river and in some rivers there will be water monitors.

			Some rivers are where people practice fishing for their living. So there are possibilities that you will be able to learn one or two fishing techniques from a native fisherman. Apart from that the rare opportunity to watch the mind-blowing view of the sinking sun from a lagoon will be possible if you take the chance to go on a boat ride.

			The most famous locations for a boat ride include Madu River in Balapitiya, Gal oya-which is the only wildlife national sanctuary which offers an opportunity to travel through boat and of course last but not least Muthurajawela. 


